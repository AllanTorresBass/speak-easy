// Function to get translation with fallback
export function getComprehensiveTranslation(word: string): string {
  const lowerWord = word.toLowerCase().trim();
  
  // Direct match
  if (comprehensivePromovaTranslations[lowerWord]) {
    return comprehensivePromovaTranslations[lowerWord];
  }
  
  // Try to find partial matches for phrases
  for (const [key, translation] of Object.entries(comprehensivePromovaTranslations)) {
    if (lowerWord.includes(key) || key.includes(lowerWord)) {
      return translation;
    }
  }
  
  // If no translation found, return empty string
  return '';
}

// Function to get multiple translations for a list of words
export function getMultipleComprehensiveTranslations(words: string[]): Record<string, string> {
  const translations: Record<string, string> = {};
  
  words.forEach(word => {
    translations[word] = getComprehensiveTranslation(word);
  });
  
  return translations;
}

// Function to get all available translations
export function getAllAvailableTranslations(): Record<string, string> {
  return { ...comprehensivePromovaTranslations };
}

// Function to get translation statistics
export function getTranslationStats(): {
  totalWords: number;
  translatedWords: number;
  coveragePercentage: number;
} {
  const totalWords = Object.keys(comprehensivePromovaTranslations).length;
  const translatedWords = Object.values(comprehensivePromovaTranslations).filter(t => t.length > 0).length;
  const coveragePercentage = (translatedWords / totalWords) * 100;
  
  return {
    totalWords,
    translatedWords,
    coveragePercentage: Math.round(coveragePercentage * 100) / 100
  };
} 